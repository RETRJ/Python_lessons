# Sometimes we need to convert a variable from one type to another. This is called casting.
# In Python, we can cast a variable to a different type.
# We can cast a variable to an int, float, str, or bool.

# To cast a variable to an int, we use the int() function.
# To cast a variable to a float, we use the float() function.
# To cast a variable to a str, we use the str() function.
# To cast a variable to a bool, we use the bool() function.

# For example:
a = 5
a_new = float(a)
# Let's make sure the variable a is an int and the variable a_new is a float.
print(a, type(a))
print(a_new, type(a_new))
